from math import sqrt


def summ(x,y):
    sum = x + y
    return sum


def subb(x,y):
    sub = x - y
    return sub


def multip(x,y):
    mult = x * y
    return mult


def divis(x,y):
    if y!=0:
        div = x / y
        return div
    else:
        return "На ноль делить нельзя"


def expon(x,y):
    exp = x ** y
    return exp


def square(x):
    squ = sqrt(x)
    return squ








